document.querySelector('#photoshop_skills').onclick = function () {
    // document.querySelector('#bootstrap_more_skills').show();

    // $('#bootstrap_more_skills').fadeIn('slow');

    if ($('#photoshop_more_skills').is(":visible")) {
        $('#photoshop_more_skills').fadeOut('slow');
    } else {
        $('#photoshop_more_skills').slideDown('slow');

    }
}

